struct Person {
    let id: Int
    let firstName: String
    let lastName: String

    static var random: Person {
        return Person(
            firstName: names[Int(arc4random()) % names.count]
            , lastName: lastnames[Int(arc4random()) % lastnames.count]
        )
    }
    
    init(firstName: String, lastName: String) {
        id = 0
        self.firstName = firstName
        self.lastName = lastName
    }
    
    init?(json: JSON) {
        if let id: Int = "id" <~~ json {
            self.id = id
        } else {
            self.id = 0
        }
        
        guard let firstName: String = "first_name" <~~ json else {
            return nil
        }
        
        self.firstName = firstName
        
        guard let lastName: String = "last_name" <~~ json else {
            return nil
        }
        
        self.lastName = lastName
    }
}

